#include "sam/types.h"

/*ARGSUSED1*/
void
cs_empty(u_longlong_t *cookie, uchar_t	*buf, int len, csum_t *val)
{
	int i;

	if (cookie != NULL) {
		/* initialization */
		for (i = 0; i < 4; i++) {
			val->csum_val[i] = 0;
		}
	}
}
